
public class switchExpamle {

	public static void main(String[] args) {
		// switch-case Örneði
			for (int i=0;i<4;i++) {
				switch(i) {
					case 0:
						System.out.println("Sayý sýfýr");
						break;
					case 1:
						System.out.println("Sayý bir");
						break;
					case 2:
						System.out.println("Sayý iki");
						break;
					default:
						System.out.println("Sayý ikiden büyük");	
				}
			}
	}

}
